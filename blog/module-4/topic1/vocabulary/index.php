            <p>
            <h5 class="text-center">Safety and Scheduled Maintenance Protect Your Welding Assets </h5>
            </p>

            <p>
              Q: <strong>What can I do to avoid electrical shocks?</strong>
            </p>
            <p>
              A: Wet working conditions must be avoided, because water is an excellent
              conductor and electricity will always follow the path of least resistance.
              Even a person’s perspiration can lower the body’s resistance to electrical
              shock. Poor connections and bare spots on cables further increase the
              possibility of electrical shock, and therefore, daily inspection of these
              items is recommended. Equipment operators should also routinely inspect for
              properground connections.
            </p>
            <p>
              Q: <strong>How can I inspect and maintain my wire feeder? </strong>
            </p>
            <p>
              A: Periodically inspect the electrode wire drive rolls. If dirty, remove
              the drive rolls and clean with a wire brush. Deformed drive rolls should be
              replaced. Drive rolls should be changed, adjusted or cleaned only when the
              wire feeder is shut off. In addition, check the inlet and outlet guides and
              replace if they are deformed from wire wear. Remember that when power is
              applied to a wire feeder, fingers should be kept away from the drive roll
              area.
            </p>
            <p>
              Q: <strong>What are some important electrode safety considerations? </strong>
            </p>
            <p>
              A: Welding power sources for use with MIG and TIG welding normally are
              equipped with devices that permit on/off control of the welding power
              output. If so, the electrode becomes electrically hot when the power source
              switch is ON and the welding gun switch is closed. Never touch the
              electrode wire or any conducting object in contact with the electrode
              circuit, unless the welding power source is off. Welding power sources used
              for shielded metal arc welding (SMAW or Stick welding) may not be equipped
              with welding power output on/off control devices. With such equipment, the
              electrode is electrically hot when the power switch is turned ON.
            </p>
            <p>
              Q: How should I store my gas cylinders?
            </p>
            <p>
              A: Cylinders should be securely fastened at all times. Chains are usually
              used to secure a cylinder to a wall or cylinder cart. When moving or
              storing a cylinder, a threaded protector cap must be fastened to the top of
              the cylinder. This protects the valve system should it be bumped or
              dropped. Cylinders should not be stored or used in a horizontal position.
              This is because some cylinders contain a liquid which would leak out or be
              forced out if the cylinder was laid in a flat position. Also, welding guns
              and other cables should not be hung on or near cylinders. A gun could cause
              an arc against the cylinder wall or valve assembly, possibly resulting in a
              weakened cylinder or even a rupture.
            </p>
            <p>
              Q: <strong>How can I tell if my regulator is faulty?</strong>
            </p>
            <p>
              A: The following symptoms indicate a faulty regulator: Leaks - if gas leaks
              externally. Excessive сreep - if delivery pressure continues to rise with
              the downstream valve closed. Faulty gauge - if gauge pointer does not move
              off the stop pin when pressurized, nor returns to the stop pin after
              pressure release. Do not attempt to repair a faulty regulator. It should be
              sent to your designated repair center, where special techniques and tools
              are used by trained personnel.
            </p>
            <p>
              Q: <strong>What are some tips for a safe welding environment?</strong>
            </p>
            <p>
              A: The area surrounding the welder will be subjected to light, heat, smoke,
              sparks and fumes. Permanent booths or portable partitions can be used to
              contain light rays in one area. The heat and sparks given off are capable
              of setting flammable materials on fire. Therefore, welding should not be
              done in areas containing flammable gases, vapors, liquids or dusty
              locations where explosions are a possibility. Metals with plating, coatings
              or paint that come near the region of the arc may give off smoke and fumes
              during welding. These fumes may pose a health hazard to the lungs,
              therefore an exhaust hood or booth should be used to remove fumes from the
              area. When welding in confined spaces, such as inside tanks, large
              containers or even compartments of a ship, toxic fumes may gather. Also, in
              an enclosed room, breathable oxygen can be replaced by shielding gases used
              for welding or purging. Care must be taken to ensure enough clean air for
              breathing. In many companies, it is routine to provide welders with air
              masks or self-contained breathing equipment.
            </p>
            <p>
              Q: <strong>How should an operator dress for optimum safety?</strong>
            </p>
            <p>
              A: Gloves and clothing should be flame-resistant. Clothing made from a
              dark-colored, tightly woven material is best suited for welding.
              Gauntlet-type leather gloves should be worn to protect the hands and
              wrists. Shirt collars and shirt cuffs should be buttoned, and open front
              pockets are not advisable as they may catch sparks. Also, operators should
              never store matches or lighters in their pockets. Pants cuffs are not
              recommended, as they will also catch sparks. Tennis shoes do not qualify as
              adequate foot protection. High-top leather shoes or boots are absolutely
              necessary.
            </p>

