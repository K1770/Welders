              Read and translate the text.
            </p>
            <p>
              In the past, symbols for welded joints were simple. It was an arrow with
              the instruction “Weld here”. It was not enough. To solve this problem,
              different welding symbols were made.
            </p>
            <p>
              The American system of symbolization is the AWS system, formulated by the
              American Welding Society (AWS). This society became widely used in the
              world, mainly because of the oil industry. Today it is used by half the
              world’s welding industry.
            </p>
            <p>
              The rest of the world uses the ISO system, designed by the International
              Organization for Standardization (ISO). That’s why we need to know the two
              systems. Welding symbols transmit information from the designer to one or
              more personsalong the quality system network. This includes the welding
              engineer, welding supervisors, welders, inspectors. You can find the
              symbols in technical specifications and reference codes.
            </p>
